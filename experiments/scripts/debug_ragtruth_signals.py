#!/usr/bin/env python3
"""
Debug RAGTruth signals to understand why AUROC is inverted.

Hypothesis: The underlying signals (A, V, D) may be inverted for RAGTruth
similar to what we saw in HaluEval diagnostics.
"""

import os
import sys
import torch
import numpy as np

sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.dirname(__file__))))

from datasets import load_dataset
from transformers import AutoModelForCausalLM, AutoTokenizer
from ag_sar import AGSAR, AGSARConfig
from ag_sar.measures import compute_varentropy, compute_semantic_dispersion


def analyze_ragtruth_signals(agsar, num_samples=30):
    """Analyze raw signals on RAGTruth QA."""
    print("\n" + "=" * 70)
    print("RAGTruth QA - Signal Analysis")
    print("=" * 70)

    dataset = load_dataset("flowaicom/RAGTruth_test", split="qa")

    # Collect signals by class
    facts = {"uncertainty": [], "A_mean": [], "T_min": [], "V_mean": [], "D_mean": []}
    halls = {"uncertainty": [], "A_mean": [], "T_min": [], "V_mean": [], "D_mean": []}

    for i in range(min(num_samples, len(dataset))):
        sample = dataset[i]
        prompt = sample["prompt"]
        response = sample["response"]
        is_hall = sample["score"] == 1

        try:
            # Get detailed results
            agsar.calibrate_on_prompt(prompt)
            result = agsar.compute_uncertainty(prompt, response, return_details=True)

            # Store by class
            target = halls if is_hall else facts
            target["uncertainty"].append(result["score"])
            # Authority is 1 - uncertainty for this API
            target["A_mean"].append(result["authority"])

        except Exception as e:
            print(f"  Error on sample {i}: {e}")
            continue

    # Print statistics
    print(f"\nSamples: {len(facts['uncertainty'])} facts, {len(halls['uncertainty'])} halls")

    print("\n--- UNCERTAINTY (should be: Facts LOW, Halls HIGH) ---")
    print(f"  Facts: {np.mean(facts['uncertainty']):.3f} ± {np.std(facts['uncertainty']):.3f}")
    print(f"  Halls: {np.mean(halls['uncertainty']):.3f} ± {np.std(halls['uncertainty']):.3f}")
    print(f"  Gap: {np.mean(halls['uncertainty']) - np.mean(facts['uncertainty']):.3f}")

    print("\n--- AUTHORITY (should be: Facts HIGH, Halls LOW) ---")
    print(f"  Facts: {np.mean(facts['A_mean']):.3f} ± {np.std(facts['A_mean']):.3f}")
    print(f"  Halls: {np.mean(halls['A_mean']):.3f} ± {np.std(halls['A_mean']):.3f}")
    print(f"  Gap: {np.mean(facts['A_mean']) - np.mean(halls['A_mean']):.3f}")

    # Interpretation
    unc_gap = np.mean(halls['uncertainty']) - np.mean(facts['uncertainty'])
    if unc_gap < 0:
        print("\n⚠️  INVERTED: Hallucinations have LOWER uncertainty than facts!")
        print("   This suggests RAGTruth responses are:")
        print("   - Generated by confident models (low V)")
        print("   - Semantically coherent even when wrong (low D)")
        print("   - Possibly from the same model family being analyzed")


def main():
    print("Loading model...")
    model_name = "meta-llama/Llama-3.1-8B-Instruct"

    tokenizer = AutoTokenizer.from_pretrained(model_name)
    if tokenizer.pad_token is None:
        tokenizer.pad_token = tokenizer.eos_token

    model = AutoModelForCausalLM.from_pretrained(
        model_name,
        torch_dtype=torch.bfloat16,
        device_map="auto",
        attn_implementation="eager",
    )

    # Test v5
    print("\n" + "#" * 70)
    print("# v5 (Dual-Path) Signal Analysis")
    print("#" * 70)
    config = AGSARConfig(version=5, aggregation_method="mean")
    agsar = AGSAR(model, tokenizer, config)
    analyze_ragtruth_signals(agsar, num_samples=30)
    agsar.cleanup()

    # Test v4 for comparison
    print("\n" + "#" * 70)
    print("# v4 (min) Signal Analysis")
    print("#" * 70)
    config = AGSARConfig(version=4, aggregation_method="min")
    agsar = AGSAR(model, tokenizer, config)
    analyze_ragtruth_signals(agsar, num_samples=30)
    agsar.cleanup()


if __name__ == "__main__":
    main()
