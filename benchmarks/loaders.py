"""
Dataset loaders for AG-SAR benchmarks.

Supports:
- HaluEval (synthetic hallucinations)
- RAGTruth (natural hallucinations from real RAG tasks)
"""

from typing import List, Dict, Optional
import numpy as np


def load_halueval(dataset_variant: str = "qa", num_samples: Optional[int] = None) -> List[Dict]:
    """
    Load HaluEval dataset (synthetic hallucinations).

    Args:
        dataset_variant: One of "qa", "summarization", "dialogue"
        num_samples: Max samples to load (None = all)

    Returns:
        List of dicts with keys: prompt, response, label (1=hallucination, 0=factual)
    """
    from datasets import load_dataset

    print(f"Loading HaluEval ({dataset_variant})...")

    ds = load_dataset("pminervini/HaluEval", dataset_variant, split="data")

    samples = list(ds)
    if num_samples and num_samples < len(samples):
        np.random.seed(42)
        indices = np.random.choice(len(samples), num_samples, replace=False)
        samples = [samples[i] for i in indices]

    # Build balanced dataset (hallucinated + correct pairs)
    eval_samples = []

    for sample in samples:
        if dataset_variant == "qa":
            prompt = f"Q: {sample['question']}\nContext: {sample.get('knowledge', '')}\nA:"

            if sample.get("hallucinated_answer"):
                eval_samples.append({
                    "prompt": prompt,
                    "response": sample["hallucinated_answer"],
                    "label": 1,
                })
            if sample.get("right_answer"):
                eval_samples.append({
                    "prompt": prompt,
                    "response": sample["right_answer"],
                    "label": 0,
                })

        elif dataset_variant == "summarization":
            doc = sample['document'][:2000] + "..." if len(sample['document']) > 2000 else sample['document']
            prompt = f"Document: {doc}\n\nSummarize the above document:"

            if sample.get("hallucinated_summary"):
                eval_samples.append({
                    "prompt": prompt,
                    "response": sample["hallucinated_summary"],
                    "label": 1,
                })
            if sample.get("right_summary"):
                eval_samples.append({
                    "prompt": prompt,
                    "response": sample["right_summary"],
                    "label": 0,
                })

        elif dataset_variant == "dialogue":
            prompt = f"Dialogue history:\n{sample['dialogue_history']}\n\nResponse:"

            if sample.get("hallucinated_response"):
                eval_samples.append({
                    "prompt": prompt,
                    "response": sample["hallucinated_response"],
                    "label": 1,
                })
            if sample.get("right_response"):
                eval_samples.append({
                    "prompt": prompt,
                    "response": sample["right_response"],
                    "label": 0,
                })

    print(f"Loaded {len(eval_samples)} samples (Hallucinated: {sum(1 for s in eval_samples if s['label'] == 1)}, Correct: {sum(1 for s in eval_samples if s['label'] == 0)})")
    return eval_samples


def load_ragtruth(split: str = "test", num_samples: Optional[int] = None, task: str = "QA") -> List[Dict]:
    """
    Load RAGTruth dataset (natural hallucinations from real RAG tasks).

    Unlike HaluEval which forces hallucinations via synthetic perturbations,
    RAGTruth contains naturally occurring hallucinations generated by models
    during real RAG tasks.

    Args:
        split: Dataset split ("train", "test")
        num_samples: Max samples to load (None = all)
        task: Filter by task_type ("QA", "Summary", "Data2txt", or None for all)

    Returns:
        List of dicts with keys: prompt, response, label (1=hallucination, 0=factual)
    """
    from datasets import load_dataset

    print(f"Loading RAGTruth ({split}, task_type={task})...")

    ds = load_dataset("wandb/RAGTruth-processed", split=split)

    # Filter by task_type if specified
    if task:
        ds = ds.filter(lambda x: x['task_type'] == task)

    # Limit samples if specified
    if num_samples and num_samples < len(ds):
        ds = ds.select(range(num_samples))

    samples = []
    for row in ds:
        # RAGTruth labels: hallucination if evident_conflict > 0 or baseless_info > 0
        labels = row['hallucination_labels_processed']
        is_hallucination = 1 if (labels['evident_conflict'] > 0 or labels['baseless_info'] > 0) else 0

        # Construct prompt from context and query
        prompt = f"Context: {row['context']}\n\nQuestion: {row['query']}\n\nAnswer:"

        samples.append({
            "prompt": prompt,
            "response": row['output'],
            "label": is_hallucination,
        })

    hallucinated = sum(1 for s in samples if s['label'] == 1)
    factual = sum(1 for s in samples if s['label'] == 0)
    print(f"Loaded {len(samples)} samples (Hallucinated: {hallucinated}, Factual: {factual})")

    return samples
